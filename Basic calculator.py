num1 = int(input("Enter the number :"))
num2 = int(input("Enter the number :"))
operation = input("Operation :")
if operation == "+" :
    print(num1 + num2)
elif operation == "-" :
    print(num1 + num2 )
elif operation == "/" :
    print(num1/num2)
elif operation == "*" :
    print(num1*num2)
