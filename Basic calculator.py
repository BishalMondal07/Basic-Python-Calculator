#write the inputs in seperate lines in proper sequence 
num1 = int(input())
num2 = int(input())
operation = input()
if operation == "+" :
    print(num1 + num2)
elif operation == "-" :
    print(num1 + num2 )
elif operation == "/" :
    print(num1/num2)
elif operation == "*" :
    print(num1*num2)